// 話者選択用のフラットな配列を生成
export function getSpeakerOptions() {
  const options = [];
  VOICEVOX_SPEAKERS.forEach(speaker => {
    speaker.styles.forEach(style => {
      options.push({
        id: style.id,
        label: `${speaker.name}(${style.name})`,
        speakerName: speaker.name,
        styleName: style.name
      });
    });
  });
  return options;
}
